		<div class="about-content">
			<p>面对中国这样一个古老、庞大而复杂的国家，从官员到农民，从时评人到的哥，似乎人人都可以评头论足一番；但是，似乎又没有人能真正将它弄明白、说清楚。大众媒体和社交网络上充斥着令人目不暇接的信息和似乎永无止境的争吵，但是，它们都太局限了。</p>
			<p>要想更靠谱地谈论中国，你需要接触更智慧的头脑，吸取更优质的思想资源。</p>

			<h3 class="about-title">我们的使命与愿景</h3>

			<p>成立于 2011 年 11 月的<strong><a href="http://cnpolitics.org/">政见</a></strong>，希望带给你这样的资源。我们是一个完全由志愿者组成的独立网站，成员包括海内外政治学研究者和国内一线时政记者。我们秉持专业、独立、理性的原则，跟踪阅读全球学术界对中国政治和社会的最新研究，从中挑选出最具价值的部分呈现给你。</p>
			<p>我们引介的思想资源，绝大部分都是在大众传媒上无法见到的。它们呆在仅有专业人士才经常问津的学术期刊、报告、会议中，普通公众鲜有机会接触。</p>
			<p>当然，来自学界的研究成果也有其局限性。但至少，比起大众传媒提供的快餐，比起社交网络上那些泛着情绪化泡沫的争吵，学界的研究有着严格的学术规范，基于丰富的研究材料，经过了理性的逻辑推演，可以在更大程度上接近真相，提供更具解释力的观点，是更值得你参考的信息。</p>
			<p>我们的努力已经产生了一定的影响，有多位大学教授在自己的课堂上使用了我们制作的内容，中央编译局出版的期刊曾转载我们的文章，还曾有省部级官员与我们联系，介绍自己的研究成果，我们也得到了包括大西洋月刊在内的众多外媒译介。不过，比起这些，我们更看重每一位普通读者从“政见”的内容中吸取的营养、得到的启发。</p>

			<h3 class="about-title">我们的风格与旨趣</h3>

			<p>我们致力于发掘理性、靠谱的思想资源，理性并不等于枯燥，它们往往是鲜活、有趣、令人茅塞顿开的。</p>
			<p>我们将普通人难以接近的学术研究成果，以强调“专业性、趣味性、信息量”的写作风格呈现给你，节省你的宝贵时间。</p>

